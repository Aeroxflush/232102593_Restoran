    private void chkbxJamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkbxJamActionPerformed
        boolean on = chkbxJam.isSelected();     // cek ON / OFF
        int row = jTable1.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
            "Pilih dulu dong datanya (｡•̀︿•́｡)",
            "Info", JOptionPane.INFORMATION_MESSAGE);
            chkbxJam.setSelected(false);
        } else if (on) {
            labelJam.setText("Loading...");
            waktu = new Waktu(labelJam);
        } else {
            waktu.stop();
            waktu = null;
            labelJam.setText(jTable1.getValueAt(row, 2).toString());
        }
    }//GEN-LAST:event_chkbxJamActionPerformed

