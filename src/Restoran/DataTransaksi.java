    private void kosongkan(){
        txtBayar.setText(null);
        txtIdTrans.setText(null);
        txtKembalian.setText(null);
        txtNama.setText(null);
        txtTotal.setText(null);
        txtCatatan.setText(null);
        txtDate.setDate(new java.util.Date());
        labelJam.setText("...");
        jTable1.clearSelection();
        chkbxJam.setSelected(false);
        if (waktu != null) waktu.stop();
    }
    
    public void settingTabel(JTable tabel){
        try {
            tabel = jTable1;
            new Koneksi().tabel(tabel); // sudah termasuk query data coy lol
            JOptionPane.showMessageDialog(null, "Koneksi berhasil", "Cihuy", JOptionPane.INFORMATION_MESSAGE);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        tabel.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabel.setDefaultEditor(Object.class, null);
    }

    public void pilihRowData(){
        int row = jTable1.getSelectedRow();

        // pilih row data langsung masuk ke txtfield masing-masing
        txtIdTrans.setText(jTable1.getValueAt(row, 0).toString());
        try {
            txtDate.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(jTable1.getValueAt(row, 1).toString()));
        } catch (ParseException ex) {
            Logger.getLogger(DataTransaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        labelJam.setText(jTable1.getValueAt(row, 2).toString());
        txtNama.setText(jTable1.getValueAt(row, 3).toString());
        txtTotal.setText(jTable1.getValueAt(row, 4).toString());
        txtBayar.setText(jTable1.getValueAt(row, 5).toString());
        txtKembalian.setText(jTable1.getValueAt(row, 6).toString());
        txtCatatan.setText(jTable1.getValueAt(row, 7).toString());
    }
