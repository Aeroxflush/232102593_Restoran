    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            int id          = Integer.parseInt(txtIdTrans.getText().trim());

            LocalDate tgl   =   txtDate.getDate().toInstant()
                                .atZone(ZoneId.of("Asia/Jakarta"))
                                .toLocalDate();

            LocalTime jam   = LocalTime.parse(labelJam.getText().trim(),
                              DateTimeFormatter.ofPattern("HH:mm:ss"));

            String  nama    = txtNama.getText().trim();
            int     total   = Integer.parseInt(txtTotal.getText().replace(".", ""));
            int     bayar   = Integer.parseInt(txtBayar.getText().replace(".", ""));
            int     kembali = Integer.parseInt(txtKembalian.getText().replace(".", ""));
            String  catatan = txtCatatan.getText();

            TransaksiService.update(id, tgl, jam, nama, total, bayar, kembali, catatan);
            
            Koneksi.pesanRefreshTabel("Data ter‑update!");
            new Koneksi().tabel(jTable1);
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

